#include <unistd.h>

int main(int argc, char **argv) {
    if (argc == 2) {
        int i = 0;
        while (argv[1][i]) {
            char c = argv[1][i];
            
            if (c >= 'a' && c <= 'z')
                c = ((c - 'a' + 1) % 26) + 'a';
            else if (c >= 'A' && c <= 'Z')
                c = ((c - 'A' + 1) % 26) + 'A';

            write(1, &c, 1);
            i++;
        }
    }
    write(1, "\n", 1);
    return 0;
}